#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1) + 1;
	s2 = (char *)malloc(len * sizeof(*s2));
	if (s2 == NULL)
		return (NULL);
	return (ft_strcpy(s2, s1));
}
