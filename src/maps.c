void		load_map(t_cont *cont, int n)
{
	static const t_load_maps	maps[NMAPS] = {
		{ (int *)g_map1, MAPH1, MAPW1 }
	};
	int							i;

	cont->board.map = xmalloc(maps[n].h * sizeof(*cont->board.map));
	cont->board.map[0] = xmalloc((maps[n].h * maps[n].w) *
			sizeof(**cont->board.map));
	cont->board.mapw = maps[n].w;
	cont->board.maph = maps[n].h;
	ft_memcpy(cont->board.map[0], maps[n].map,
			maps[n].w * maps[n].h * sizeof(*maps[n].map));
	i = 1;
	while (i < maps[n].h)
	{
		cont->board.map[i] = cont->board.map[0] + (i * maps[n].w);
		i++;
	}
}
